    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        Admin a = new Admin();
        
        String plainPassword = new String(txtPassword.getPassword()); //to  convert the password into string;
        
        boolean repeatMail =isRepeatEmail(txtEmail.getText());
        boolean validMail = a.validateEmail(txtEmail.getText());
        boolean validPhoneNumber = a.validatePhoneNumber(txtPhone.getText());
        
        //changed validiation method (HG Lim)
        if (txtEmail.getText().length()==0||txtUsername.getText().length()==0|txtPhone.getText().length()==0||plainPassword.length()==0){
            JOptionPane.showMessageDialog(null,"Please fill in all details");
        } 
        else if (repeatMail)
        {
            JOptionPane.showMessageDialog(null, "Email is taken.");
        }
        else if (!validMail)
        {
            JOptionPane.showMessageDialog(null, "Invalid email format.");
            
        }
        else if (!validPhoneNumber)
        {
            JOptionPane.showMessageDialog(null, "Invalid phone number format.");
            
        }
        else
        {
            //get full userList to be overwritten
            ArrayList<ArrayList<String>> userList = a.getFullList();
            String userid = IOMethods.updateIndex("C", 2);
            ArrayList <String> newUser = new ArrayList<>();
            newUser.add(userid);
            newUser.add(txtUsername.getText());
            newUser.add(txtPhone.getText());
            newUser.add(txtEmail.getText()); //getting username from textfield
            newUser.add(plainPassword);//to get the password in string
            newUser.add("C");
            userList.add(newUser);
            IOMethods.writeArrayListOfArrayList(IOMethods.USERTEXT, userList);
            JOptionPane.showMessageDialog(null, "New user " + txtUsername.getText() + " created.");
            this.dispose();
            new Login().setVisible(true);

        }
        
        
        
        
    }//GEN-LAST:event_btnRegisterActionPerformed

