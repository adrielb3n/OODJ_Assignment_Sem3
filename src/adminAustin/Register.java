    public boolean isRepeatEmail(String newEmail)
    {
        boolean repeat = false;
        Admin a = new Admin();
        
        //get userList without master admin account
        ArrayList<ArrayList<String>> userList = a.getUserList();
        for (ArrayList<String> user: userList)
        {
            if (newEmail.equals(user.get(3)))
            {
                repeat = true;
            }
            
        }
        return repeat;
    }

